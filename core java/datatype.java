public class datatype
{
   public static void main(String[] args)
   {
      byte    a;         //declaration of variable
              a=20;           //initialization of variable    
      short   b=30;
      int    c=40;
      long   d=5L;    //(used L or l as a suffix of long value are not madatory)
      float  e=4.5f;  //(use F or f used as suffix it is madatory)
      double f=22.34d;//(used "d" or "d" used as suffix it is not madatory)
      char   g='A';
      boolean h=true; 
      System.out.println(a);
      System.out.println(b);
      System.out.println(c);
      System.out.println(d);
      System.out.println(e);
      System.out.println(f);
      System.out.println(g);    
       System.out.println(h); 
   }
}